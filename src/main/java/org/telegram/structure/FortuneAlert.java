package org.telegram.structure;

/**
 * @author Ruben Bermudez
 * @version 1.0
 */
public class FortuneAlert {
	private int id;
	private int userId;

	public FortuneAlert() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}
}
